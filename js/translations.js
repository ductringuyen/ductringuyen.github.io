let currentLang = 'en';

// Function to switch language
function switchLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('preferredLang', lang);

    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.dataset.lang === lang) {
            btn.classList.add('active');
        }
    });

    // Update all translatable elements
    const t = translations[lang];

    // Navigation
    document.querySelector('[data-nav-section="about"]').textContent = t.nav_about;
    document.querySelector('[data-nav-section="services"]').textContent = t.nav_services;
    document.querySelector('[data-nav-section="skills"]').textContent = t.nav_skills;
    document.querySelector('[data-nav-section="education"]').textContent = t.nav_education;
    document.querySelector('[data-nav-section="experience"]').textContent = t.nav_experience;
    document.querySelector('[data-nav-section="contact"]').textContent = t.nav_contact;

    // Sidebar
    document.getElementById('available-badge').textContent = t.available_badge;

    // About section
    const aboutSection = document.querySelector('[data-section="about"]');
    if (aboutSection) {
        const aboutTitle = aboutSection.querySelector('h2');
        if (aboutTitle) aboutTitle.textContent = t.about_title;

        const aboutDesc = aboutSection.querySelector('.about-desc');
        if (aboutDesc) {
            const paragraphs = aboutDesc.querySelectorAll('p');
            if (paragraphs[0]) paragraphs[0].innerHTML = t.about_intro;
            if (paragraphs[1]) paragraphs[1].innerHTML = t.about_specialization;
            if (paragraphs[2]) paragraphs[2].innerHTML = t.about_available;
            if (paragraphs[3]) paragraphs[3].textContent = t.about_expertise;
        }

        // Service boxes in about
        const serviceBoxes = aboutSection.querySelectorAll('.services');
        if (serviceBoxes[0]) {
            serviceBoxes[0].querySelector('h3').innerHTML = t.service1_title;
            serviceBoxes[0].querySelector('p').textContent = t.service1_desc;
        }
        if (serviceBoxes[1]) {
            serviceBoxes[1].querySelector('h3').innerHTML = t.service2_title;
            serviceBoxes[1].querySelector('p').textContent = t.service2_desc;
        }
        if (serviceBoxes[2]) {
            serviceBoxes[2].querySelector('h3').innerHTML = t.service3_title;
            serviceBoxes[2].querySelector('p').textContent = t.service3_desc;
        }
        if (serviceBoxes[3]) {
            serviceBoxes[3].querySelector('h3').innerHTML = t.service4_title;
            serviceBoxes[3].querySelector('p').textContent = t.service4_desc;
        }
    }

    // Services section
    const servicesSection = document.querySelector('[data-section="services"]');
    if (servicesSection) {
        const servicesTitle = servicesSection.querySelector('h2');
        if (servicesTitle) servicesTitle.textContent = t.services_title;

        const servicesIntro = servicesSection.querySelector('.colorlib-narrow-content > p');
        if (servicesIntro) servicesIntro.textContent = t.services_intro;

        const serviceCards = servicesSection.querySelectorAll('.service-card');
        if (serviceCards[0]) {
            serviceCards[0].querySelector('h4').textContent = t.svc_ai_title;
            serviceCards[0].querySelectorAll('p')[0].textContent = t.svc_ai_desc;
            const items = serviceCards[0].querySelectorAll('li');
            if (items[0]) items[0].textContent = t.svc_ai_1;
            if (items[1]) items[1].textContent = t.svc_ai_2;
            if (items[2]) items[2].textContent = t.svc_ai_3;
        }
        if (serviceCards[1]) {
            serviceCards[1].querySelector('h4').textContent = t.svc_cloud_title;
            serviceCards[1].querySelectorAll('p')[0].textContent = t.svc_cloud_desc;
            const items = serviceCards[1].querySelectorAll('li');
            if (items[0]) items[0].textContent = t.svc_cloud_1;
            if (items[1]) items[1].textContent = t.svc_cloud_2;
            if (items[2]) items[2].textContent = t.svc_cloud_3;
        }
        if (serviceCards[2]) {
            serviceCards[2].querySelector('h4').textContent = t.svc_backend_title;
            serviceCards[2].querySelectorAll('p')[0].textContent = t.svc_backend_desc;
            const items = serviceCards[2].querySelectorAll('li');
            if (items[0]) items[0].textContent = t.svc_backend_1;
            if (items[1]) items[1].textContent = t.svc_backend_2;
            if (items[2]) items[2].textContent = t.svc_backend_3;
        }
        if (serviceCards[3]) {
            serviceCards[3].querySelector('h4').textContent = t.svc_data_title;
            serviceCards[3].querySelectorAll('p')[0].textContent = t.svc_data_desc;
            const items = serviceCards[3].querySelectorAll('li');
            if (items[0]) items[0].textContent = t.svc_data_1;
            if (items[1]) items[1].textContent = t.svc_data_2;
            if (items[2]) items[2].textContent = t.svc_data_3;
        }

        // Trusted section
        const trustedSection = servicesSection.querySelector('[data-animate-effect="fadeInUp"]');
        if (trustedSection) {
            const trustedTitle = trustedSection.querySelector('h4');
            if (trustedTitle) trustedTitle.textContent = t.trusted_title;
            const trustedDesc = trustedSection.querySelector('p');
            if (trustedDesc) trustedDesc.textContent = t.trusted_desc;
        }
    }

    // Skills section
    const skillsSection = document.querySelector('[data-section="skills"]');
    if (skillsSection) {
        const skillsTitle = skillsSection.querySelector('h2');
        if (skillsTitle) skillsTitle.textContent = t.skills_title;

        const skillsIntro = skillsSection.querySelector('.colorlib-narrow-content > p');
        if (skillsIntro) skillsIntro.textContent = t.skills_intro;

        const skillHeaders = skillsSection.querySelectorAll('h4');
        if (skillHeaders[0]) skillHeaders[0].innerHTML = '<i class="fa-solid fa-code"></i> ' + t.skills_languages;
        if (skillHeaders[1]) skillHeaders[1].innerHTML = '<i class="fa-solid fa-brain"></i> ' + t.skills_ai;
        if (skillHeaders[2]) skillHeaders[2].innerHTML = '<i class="fa-solid fa-cloud"></i> ' + t.skills_cloud;
        if (skillHeaders[3]) skillHeaders[3].innerHTML = '<i class="fa-solid fa-database"></i> ' + t.skills_db;
        if (skillHeaders[4]) skillHeaders[4].innerHTML = '<i class="fa-solid fa-chart-line"></i> ' + t.skills_monitoring;
    }

    // Education section
    const educationSection = document.querySelector('[data-section="education"]');
    if (educationSection) {
        const eduTitle = educationSection.querySelector('h2');
        if (eduTitle) eduTitle.textContent = t.education_title;

        const degreeLink = educationSection.querySelector('.panel-title a');
        if (degreeLink) degreeLink.textContent = t.edu_degree;

        const panelBody = educationSection.querySelector('.panel-body');
        if (panelBody) {
            const paragraphs = panelBody.querySelectorAll('p');
            if (paragraphs[0]) paragraphs[0].innerHTML = '<strong><a href="https://www.tu-berlin.de/menue/home/">' + t.edu_university + '</a></strong> | ' + t.edu_years;
            if (paragraphs[1]) paragraphs[1].innerHTML = '<strong>' + t.edu_focus + '</strong> ' + t.edu_focus_areas;
            if (paragraphs[2]) paragraphs[2].textContent = t.edu_ranking;
        }
    }

    // Experience section
    const experienceSection = document.querySelector('[data-section="experience"]');
    if (experienceSection) {
        const expTitle = experienceSection.querySelector('h2');
        if (expTitle) expTitle.textContent = t.experience_title;
    }

    // Contact section
    const contactSection = document.querySelector('[data-section="contact"]');
    if (contactSection) {
        const contactTitle = contactSection.querySelector('h2');
        if (contactTitle) contactTitle.textContent = t.contact_title;

        const contactIntro = contactSection.querySelector('.row-bottom-padded-sm > p');
        if (contactIntro) contactIntro.textContent = t.contact_intro;

        const contactInfos = contactSection.querySelectorAll('.contact-info');
        if (contactInfos[0]) {
            contactInfos[0].querySelector('h4').innerHTML = '<i class="fa-solid fa-envelope"></i> ' + t.contact_email;
        }
        if (contactInfos[1]) {
            contactInfos[1].querySelector('h4').innerHTML = '<i class="fa-solid fa-phone"></i> ' + t.contact_phone;
        }
        if (contactInfos[2]) {
            contactInfos[2].querySelector('h4').innerHTML = '<i class="fa-solid fa-location-dot"></i> ' + t.contact_location;
            const ps = contactInfos[2].querySelectorAll('p');
            if (ps[0]) ps[0].textContent = t.contact_location_value;
            if (ps[1]) ps[1].textContent = t.contact_location_note;
        }
        if (contactInfos[3]) {
            contactInfos[3].querySelector('h4').innerHTML = '<i class="fa-solid fa-globe"></i> ' + t.contact_languages;
            const ps = contactInfos[3].querySelectorAll('p');
            if (ps[0]) ps[0].innerHTML = '<strong>' + t.contact_german + '</strong> ' + t.contact_fluent + ' (C1)';
            if (ps[1]) ps[1].innerHTML = '<strong>' + t.contact_english + '</strong> ' + t.contact_fluent + ' (C2)';
            if (ps[2]) ps[2].innerHTML = '<strong>' + t.contact_vietnamese + '</strong> ' + t.contact_native;
        }

        // CTA section
        const ctaSection = contactSection.querySelector('[style*="background: linear-gradient"]');
        if (ctaSection) {
            const ctaTitle = ctaSection.querySelector('h4');
            if (ctaTitle) ctaTitle.textContent = t.contact_cta_title;
            const ctaRate = ctaSection.querySelector('p');
            if (ctaRate) ctaRate.textContent = t.contact_cta_rate;
            const ctaBtn = ctaSection.querySelector('a');
            if (ctaBtn) ctaBtn.textContent = t.contact_cta_button;
        }
    }
}

// Initialize language switcher
document.addEventListener('DOMContentLoaded', function() {
    // Check for saved language preference
    const savedLang = localStorage.getItem('preferredLang');
    if (savedLang && translations[savedLang]) {
        switchLanguage(savedLang);
    }

    // Add click handlers to language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            switchLanguage(this.dataset.lang);
        });
    });
});
